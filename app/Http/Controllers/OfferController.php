    /**
     * Store a newly created resource in storage.
     *
     * @param  \App\Http\Requests\StoreOfferRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'requestID' => ['required', 'string'],
            'remarks' => ['required', 'string'],
        ]);

        $offer = new Offer;

        $offer->offerDate = date('Y-m-d');
        $offer->remarks = $request->remarks;
        $offer->offerStatus = false;
        $offer->requestID = $request->requestID;
        $offer->username = auth()->user()->username;

        $offer->save();

        return redirect()->back()->with('message', 'New offer has been successfully created.');;
    }

